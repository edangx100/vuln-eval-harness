"""Comparison report generator: a Markdown view over two or more saved runs.

This is one human-readable report that lets a reader compare two or more models at a
glance: which model detects more vulnerabilities, which one is more reliable across repeated
attempts, which one actually gets the exploit to fire (Layer 2), and which one is more prone to
false alarms. One hard constraint holds: **given the same result files, the report
must come out identical every time** — no timestamps, no random ordering, no "current time" text.

**"The report is a view, not a source of truth".** Every number this module prints is
read straight off the :class:`~src.results.RunResult` objects that :mod:`src.results` already
computed and saved — this module does no scoring, no averaging, and no re-derivation of its own. If
two people run this generator over the same two result files, they get the exact same report,
because the only inputs are the files themselves. That is also why this module takes
already-loaded :class:`~src.results.RunResult` objects rather than file paths: reading files is the
caller's job (the ``compare_results.py`` entry point); this module's job is purely the
Markdown rendering, which keeps it trivially unit-testable against in-memory fixtures.

**Layered, never collapsed.** The report mirrors the harness's own discipline: the
capability-core table, the Layer-2 pass-rate table, and the false-positive rate are kept in
separate sections rather than blended into one score — the same "keep the buckets separate"
principle :mod:`src.scorer` and :mod:`src.results` already follow.

**Qualitative notes are rule-based, not an LLM judge.** The brief qualitative failure
analysis is generated by simple, deterministic rules over the stored data (a
missed detection, a false positive, a failed Layer-2 reproduction with its recorded reason) — never
by asking a model to summarize. That keeps the report reproducible and free of the very
LLM-judge step the determinism requirement forbids in core metrics.
"""

from __future__ import annotations

from src.results import CaseResult, RunResult

# Caps how many qualitative failure notes are printed per run, keeping the section brief
# even for a run with many failing cases. A rule-based generator has no natural stopping
# point of its own, so this is the explicit limit that keeps the report scannable.
_MAX_FAILURE_NOTES_PER_RUN = 5

# The three Layer-1 capability-core dimensions, in the fixed order they are always displayed:
# detection, vulnerability type, CWE.
_CORE_DIMENSIONS = ("detection", "type", "cwe")

# Human-readable label for each dimension key, used only for table headers (never for scoring).
_DIMENSION_LABELS = {
    "detection": "Detection",
    "type": "Vulnerability type",
    "cwe": "CWE",
}


def generate_comparison(runs: list[RunResult]) -> str:
    """Render a Markdown comparison report over two or more runs.

    A pure function of its input: calling it twice with the same (or equal-valued) list of
    :class:`~src.results.RunResult` objects returns byte-identical Markdown, because every number
    printed is read directly off those objects and every list this function iterates is explicitly
    sorted first (dict ordering is never relied on) — satisfying the reproducibility
    requirement.

    Args:
        runs: The runs to compare, in the order their columns should appear. Must contain at least
            one run; a single run produces a (degenerate but valid) one-column report.

    Returns:
        The full report as a Markdown string, containing: the compared models and run metadata, the
        trial-averaged per-dimension accuracies and overall capability score, the separately
        reported function-location and false-positive figures, the Layer-2 pass rate per case, the
        per-case reliability (pass rate across attempts), and a brief qualitative failure analysis.

    Raises:
        ValueError: If ``runs`` is empty — there is nothing to compare.
    """
    if not runs:
        raise ValueError("generate_comparison requires at least one run to report on")

    sections = [
        _title_and_metadata(runs),
        _headline_metrics_table(runs),
        _layer2_pass_rate_table(runs),
        _reliability_section(runs),
        _qualitative_notes_section(runs),
    ]
    # Join with blank lines between sections so the Markdown renders with clear visual breaks;
    # a trailing newline keeps the file well-formed for tools that expect one (e.g. diff, git).
    return "\n\n".join(sections) + "\n"


def _title_and_metadata(runs: list[RunResult]) -> str:
    """Build the report title and the one-line run metadata (models + attempts used).

    Args:
        runs: The runs being compared.

    Returns:
        A Markdown title followed by a metadata line naming the corpus, sandbox image(s), and
        attempts-per-case for each run.
    """
    lines = ["# Model Comparison — Python Vulnerability Discovery", ""]
    for run in runs:
        lines.append(
            f"- **{run.model_id}** (run `{run.run_name}`) — corpus `{run.corpus_version}`, "
            f"sandbox `{run.sandbox_image}`, {run.attempts_per_case} attempts/case"
        )
    return "\n".join(lines)


def _pct(value: float | None) -> str:
    """Format a 0..1 fraction as a percentage string, or ``"n/a"`` when nothing was measured.

    Args:
        value: A fraction in ``[0, 1]``, or ``None`` when the underlying figure has no denominator
            (e.g. a run with no negative controls has no false-positive rate).

    Returns:
        ``"n/a"`` for ``None``; otherwise the percentage to one decimal place, e.g. ``"90.0%"``.
    """
    if value is None:
        return "n/a"
    return f"{value * 100:.1f}%"


def _headline_metrics_table(runs: list[RunResult]) -> str:
    """Build the main metrics table: one row per dimension, one column per run.

    Reads every value straight from each run's already-computed
    :attr:`~src.results.RunResult.aggregate` — no recomputation happens here. Rows are, in order:
    the overall capability core (with its raw/max shown for context), each of the three core
    dimensions, the separately reported function-location score, and the separately reported
    false-positive rate ('s "keep the buckets separate" rule, carried into the report).

    Args:
        runs: The runs being compared.

    Returns:
        A Markdown table.
    """
    header = "| Metric | " + " | ".join(run.model_id for run in runs) + " |"
    divider = "|---|" + "---|" * len(runs)

    core_row_cells = []
    for run in runs:
        agg = run.aggregate
        if agg.capability_core_pct is None:
            core_row_cells.append("n/a")
        else:
            core_row_cells.append(
                f"{_pct(agg.capability_core_pct)} "
                f"({agg.capability_core_raw:.1f}/{agg.capability_core_max:.0f})"
            )
    rows = ["| Capability core (%) | " + " | ".join(core_row_cells) + " |"]

    # One row per core dimension, reading each run's per_dimension_pct dict by the same fixed key.
    for dimension in _CORE_DIMENSIONS:
        cells = [_pct(run.aggregate.per_dimension_pct.get(dimension)) for run in runs]
        rows.append(f"| {_DIMENSION_LABELS[dimension]} | " + " | ".join(cells) + " |")

    rows.append(
        "| Function location (sep.) | "
        + " | ".join(_pct(run.aggregate.function_location_pct) for run in runs)
        + " |"
    )
    rows.append(
        "| False-positive rate (neg. controls) | "
        + " | ".join(_pct(run.aggregate.false_positive_rate) for run in runs)
        + " |"
    )

    return "\n".join([header, divider, *rows])


def _all_layer2_case_ids(runs: list[RunResult]) -> list[str]:
    """The sorted union of every Layer-2-eligible case id across all runs.

    Filtered by each :class:`~src.results.CaseResult`'s own ``layer2_eligible`` flag — not by
    presence in :attr:`~src.results.RunResult.layer2_pass_rate`, which carries a ``None`` entry for
    *every* case (eligible or not) so "not measured" stays distinct from "measured, 0% passed".
    Sorted explicitly (rather than iterating a dict) so the table's row order never depends on
    dict insertion order — part of what keeps the report reproducible.

    Args:
        runs: The runs being compared.

    Returns:
        Case ids in sorted order, deduplicated across runs.
    """
    case_ids: set[str] = set()
    for run in runs:
        case_ids.update(case.case_id for case in run.cases if case.layer2_eligible)
    return sorted(case_ids)


def _layer2_pass_rate_table(runs: list[RunResult]) -> str:
    """Build the Layer-2 pass-rate table: one row per Layer-2 case, one column per run.

    Kept as its own table, separate from the headline metrics table, mirroring the harness's own
    separation of Layer 1 (a claim) from Layer 2 (evidence the claim actually holds). A
    run with no Layer-2-eligible cases still produces a (header-only) table so the report's
    structure never depends on which cases happen to be eligible.

    Args:
        runs: The runs being compared.

    Returns:
        A Markdown table, or a one-line note if no run has any Layer-2 case.
    """
    case_ids = _all_layer2_case_ids(runs)
    if not case_ids:
        return "## Layer-2 pass rate\n\nNo Layer-2-eligible cases in this comparison."

    header = "| Case | " + " | ".join(run.model_id for run in runs) + " |"
    divider = "|---|" + "---|" * len(runs)
    rows = []
    for case_id in case_ids:
        # A run where this case was never reproduced (not eligible, or no attempt ran) reads as
        # "n/a" rather than 0% — "not measured" must stay visually distinct from "measured, failed".
        cells = [_pct(run.layer2_pass_rate.get(case_id)) for run in runs]
        rows.append(f"| {case_id} | " + " | ".join(cells) + " |")

    return "\n".join(["## Layer-2 pass rate", "", header, divider, *rows])


def _case_reliability_line(case: CaseResult) -> str:
    """One case's per-dimension reliability as a compact "dim k/N" phrase.

    Only dimensions the case actually had a pass rate for are shown (a negative control has no
    ``type``/``cwe`` entry) — an omitted dimension means "not applicable", never "0/N".

    Args:
        case: One run's result for a single case.

    Returns:
        A phrase like ``"detection 3/3, cwe 2/3, layer2 3/3"``, in a fixed dimension order so the
        same case always renders identically.
    """
    # Fixed order (not dict iteration order) keeps this line's word order stable across runs.
    ordered_dims = ("detection", "type", "cwe", "function", "layer2")
    parts = []
    for dim in ordered_dims:
        rate = case.pass_rates.get(dim)
        if rate is not None:
            parts.append(f"{dim} {rate.correct}/{rate.total}")
    return ", ".join(parts)


def _reliability_section(runs: list[RunResult]) -> str:
    """Build the "reliability across attempts" section.

    One bullet per case, listing every run's per-dimension pass rate for that case side by side —
    e.g. ``"CASE-01: run-a detection 3/3, cwe 3/3; run-b detection 3/3, cwe 2/3."`` — so an
    inconsistent model (right sometimes, wrong other times) is visible next to a reliable one,
    rather than both collapsing into the same trial-averaged percentage shown in the headline table.

    Args:
        runs: The runs being compared.

    Returns:
        A Markdown section with one bullet per case (sorted by case id).
    """
    case_ids = sorted({case.case_id for run in runs for case in run.cases})
    lines = [f"## Reliability (pass-rate across {runs[0].attempts_per_case} attempts)", ""]
    for case_id in case_ids:
        per_run_phrases = []
        for run in runs:
            case = next((c for c in run.cases if c.case_id == case_id), None)
            if case is not None:
                per_run_phrases.append(f"{run.model_id} {_case_reliability_line(case)}")
        lines.append(f"- {case_id}: " + "; ".join(per_run_phrases) + ".")
    return "\n".join(lines)


def _failure_notes(run: RunResult) -> list[str]:
    """Rule-based, deterministic failure notes for one run ('s "qualitative" analysis).

    Scans the run's cases and attempts in a fixed order (case id, then attempt index — never dict
    order) and records the first :data:`_MAX_FAILURE_NOTES_PER_RUN` notable misses it finds:

      - a negative control wrongly flagged as vulnerable (a false positive), or
      - a vulnerable case where detection itself was missed, or
      - a Layer-2 reproduction that ran but failed, tagged with its recorded taxonomy reason
 — e.g. ``WRONG_TARGET`` for a PoC that faked the effect in its own code.

    Every note is built entirely from fields already stored on the run (the score, the outcome, the
    attempt index) — no free text is invented and no model is asked to summarize, so the notes are
    exactly reproducible from the same result file.

    Args:
        run: The run to scan.

    Returns:
        Up to :data:`_MAX_FAILURE_NOTES_PER_RUN` short, human-readable note strings.
    """
    notes: list[str] = []
    for case in sorted(run.cases, key=lambda c: c.case_id):
        for attempt in sorted(case.attempts, key=lambda a: a.attempt_index):
            if len(notes) >= _MAX_FAILURE_NOTES_PER_RUN:
                return notes

            if case.is_negative_control:
                if attempt.score.detection_correct is False:
                    notes.append(
                        f"{run.model_id} on {case.case_id} (attempt {attempt.attempt_index}): "
                        "flagged safe code as vulnerable (false positive)."
                    )
                continue

            if attempt.score.detection_correct is False:
                notes.append(
                    f"{run.model_id} on {case.case_id} (attempt {attempt.attempt_index}): "
                    "missed the vulnerability entirely."
                )
            elif attempt.outcome is not None and not attempt.outcome.passed:
                notes.append(
                    f"{run.model_id} on {case.case_id} (attempt {attempt.attempt_index}): "
                    f"correct diagnosis, but the Layer-2 reproduction failed "
                    f"({attempt.outcome.reason.value})."
                )
    return notes


def _qualitative_notes_section(runs: list[RunResult]) -> str:
    """Build the "qualitative failure notes" section, concatenating each run's notes.

    Args:
        runs: The runs being compared.

    Returns:
        A Markdown section listing up to :data:`_MAX_FAILURE_NOTES_PER_RUN` notes per run, or a
        one-line note if nothing notable was found in any run.
    """
    all_notes = [note for run in runs for note in _failure_notes(run)]
    lines = ["## Qualitative failure notes", ""]
    if not all_notes:
        lines.append("No notable failures recorded.")
    else:
        lines.extend(f"- {note}" for note in all_notes)
    return "\n".join(lines)
